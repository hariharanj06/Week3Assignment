package week3.day5.assignment;

public class Swapping {
public static void main(String[] args) {
	//Declare a varaible a & b values
	int a = 40;
	int b = 50;
	
	//Declare third variable for swap 
	int c = a;
	a = b;
	b = c;
	    
	    //print swap values
	    System.out.println("a = " +a);
	    System.out.println("b = "  +b);
}
}
